package com.qianfeng.recycle.vo;

public class DamageInfoVO {

    private Long infoId;
    private String damageInfo;
    private String damageInfoDescrible;

    public Long getInfoId() {
        return infoId;
    }

    public void setInfoId(Long infoId) {
        this.infoId = infoId;
    }

    public String getDamageInfo() {
        return damageInfo;
    }

    public void setDamageInfo(String damageInfo) {
        this.damageInfo = damageInfo;
    }

    public String getDamageInfoDescrible() {
        return damageInfoDescrible;
    }

    public void setDamageInfoDescrible(String damageInfoDescrible) {
        this.damageInfoDescrible = damageInfoDescrible;
    }
}
